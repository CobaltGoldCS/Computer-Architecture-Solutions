    main:
        # First clear the entire screen
        lw R1, screenStart
        lw R2, screenEnd
        addi R3, R0, -1
        clearLoop:
            sw R3, 0(R1)
            addi R1, R1, 1
            bne R1, R2, clearLoop

        # Draw initial frame
        addi R1, R0, 3      # Start at first data address
        lw R2, 0(R1)        # Load initial black pixel count
        addi R1, R1, 1      # Point to first pixel address
        add R3, R0, R0      # Initialize counter

        drawInitial:
            beq R3, R2, initialDone
            lw R4, 0(R1)    # Load pixel address
            sw R0, 0(R4)    # Draw black pixel
            addi R1, R1, 1  # Next address
            addi R3, R3, 1  # Increment counter
            j drawInitial

        initialDone:
            lw R1, frameDataStart  # Load start of frame difference data
            add R7, R0, R0        # Current frame number
            display

        nextFrame:

            # Draw black pixels first
            lw R2, 0(R1)      # Load black pixel count
            addi R1, R1, 1
            add R3, R0, R0    # Counter for black pixels

            drawBlack:
                beq R3, R2, blackDone
                lw R4, 0(R1)    # Load pixel address
                sw R0, 0(R4)    # Draw black pixel
                addi R1, R1, 1  # Next address
                addi R3, R3, 1  # Increment counter
                j drawBlack

            blackDone:
            # Now draw white pixels
            lw R2, 0(R1)      # Load white pixel count
            addi R1, R1, 1
            add R3, R0, R0    # Counter for white pixels
            addi R4, R0, -1   # White pixel value

            drawWhite:
                beq R3, R2, whiteDone
                lw R5, 0(R1)    # Load pixel address
                sw R4, 0(R5)    # Draw white pixel
                addi R1, R1, 1  # Next address
                addi R3, R3, 1  # Increment counter
                j drawWhite

            whiteDone:
                display

                # Increment frame counter and check for reset
                addi R7, R7, 1
                lw R2, numFrames
                bne R7, R2, continue

                # Reset frame counter and memory pointer
                add R7, R0, R0        # Reset frame counter to 0
                lw R1, frameDataStart  # Reset memory pointer to start of frame data

            continue:
                j nextFrame
    